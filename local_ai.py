def get_local_reply(
    incoming: str,
    history: list = None,
    persona: str = "timur",
) -> str:
    """
    Локальный AI ответ. Без интернета, без API.

    Цепочка:
      1. TF-IDF по реальным диалогам (smart_brain)
      2. Марков-цепь генерация (markov_ai)
      3. Шаблоны по ключевым словам
    """
    exclude: set[str] = set()
    if history:
        exclude = {t.lower().strip() for r, t in history[-30:] if r == "assistant"}

    # ── 1. TF-IDF по реальным диалогам ──────
    if _smart_brain_ok:
        try:
            reply = get_smart_reply(persona, incoming, history)
            if reply and reply.lower().strip() not in exclude:
                return reply
        except Exception as e:
            print(f"⚠️ smart_brain ошибка: {e}")

    # ── 2. Марков-цепь ───────────────────────
    if _markov_ok:
        try:
            reply = get_markov_reply(persona, exclude)
            if reply:
                return reply
        except Exception as e:
            print(f"⚠️ markov_ai ошибка: {e}")

    # ── 3. Шаблоны (всегда работает) ─────────
    return _template_reply(incoming)


def get_local_reply_stats() -> dict:
    """Статистика локального AI."""
    stats = {
        "smart_brain": _smart_brain_ok,
        "markov": _markov_ok,
    }
    if _smart_brain_ok:
        try:
            from smart_brain import get_smart_stats
            stats["pairs"] = get_smart_stats()
        except Exception:
            pass
    return stats
