def _template_reply(text: str) -> str:
    """Шаблонный ответ по ключевым словам."""
    t = text.lower().strip()

    for keywords, replies in _SMART_REPLIES.items():
        if any(kw in t for kw in keywords):
            return random.choice(replies)

    if t.endswith("?") or t.startswith(("почему", "как", "зачем", "когда", "где")):
        return random.choice([
            "Хороший вопрос. Что думаете?",
            "Зависит от ситуации",
            "Расскажите подробнее",
            "Интересно. И что?",
            "Как посмотреть",
        ])

    if len(t.split()) <= 2:
        return random.choice([
            "Это как?",
            "Расскажите",
            "И что дальше?",
            "Понял",
            "Интересно",
            "Серьёзно?",
            "Продолжайте",
        ])

    return random.choice(_FALLBACK)


